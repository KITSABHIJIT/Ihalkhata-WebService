package domainmodel;


public class Notification {

	public String date;
	public String itemList;
	public String price;
	public String shareholderId;
	public String shareholderName;
	public String paidById;
	public String paidByName;
	public String count;
	public String perHead;
	public String desc;
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getItemList() {
		return itemList;
	}
	public void setItemList(String itemList) {
		this.itemList = itemList;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getShareholderId() {
		return shareholderId;
	}
	public void setShareholderId(String shareholderId) {
		this.shareholderId = shareholderId;
	}
	public String getShareholderName() {
		return shareholderName;
	}
	public void setShareholderName(String shareholderName) {
		this.shareholderName = shareholderName;
	}
	public String getPaidById() {
		return paidById;
	}
	public void setPaidById(String paidById) {
		this.paidById = paidById;
	}
	public String getPaidByName() {
		return paidByName;
	}
	public void setPaidByName(String paidByName) {
		this.paidByName = paidByName;
	}
	public String getCount() {
		return count;
	}
	public void setCount(String count) {
		this.count = count;
	}
	public String getPerHead() {
		return perHead;
	}
	public void setPerHead(String perHead) {
		this.perHead = perHead;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
}
